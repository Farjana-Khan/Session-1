# Assignment 6: Create a tuple with mixed data types and demonstrate its potential use cases in data structures like dictionaries.

# Create a tuple with mixed data types
my_tuple = ("Borno", 20, "Computer Science")

# Use the tuple as a dictionary key
my_dict = {my_tuple: "Excellent"}

# Access and print the dictionary value using the tuple as the key
print("Tuple:", my_tuple)
print("Value in dictionary:", my_dict[my_tuple])
